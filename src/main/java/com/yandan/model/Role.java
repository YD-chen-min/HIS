package com.yandan.model;
//角色信息
public class Role {
    private String id;//角色id
    private String name;//角色名称

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
